DMA_SCORE
;START SCORE DIRECT DMA

	MOVE	@STATUS,A0
	SRL	1,A0
	JRC	NOMSG1
	MOVI	P1PID,A0
	CLR	A1
	NOT	A1
	CALLA	KILALL
	MOVI	P1DATA,A11
	CREATE	P1PID,MSG_PROC
NOMSG1:
	MOVE	@STATUS,A0
	SRL	2,A0
	JRC	NOMSG2
	MOVI	P2PID,A0
	CLR	A1
	NOT	A1
	CALLA	KILALL
	MOVI	P2DATA,A11
	CREATE	P2PID,MSG_PROC		;ALWAYS DISPLAY START STUFF IN ATTRACT
NOMSG2:
	MOVK	1,A0
	MOVE	A0,@SCORE_FLAG		;1=SHOW DMA SCORE AREA ON SCREEN
	CREATE0	KILL_IMAGES
	RETS

KILL_IMAGES
	SLEEPK	3
;KILL SCOREBOARD IMAGES
	MOVI	CLSNEUT|TYPTEXT|SUBSCOR,A0
	CLR	A1
	NOT	A1
	CALLA	KILOBJ
	DIE

ONARRW:
;TURN ON AN ARROW AND BLINK IT FOREVER UNTIL SOMEBODY ELSE KILLS IT
;A9=0 FOR UP ARROW, 1=RGT ARROW, 2=LFT ARROW
	SLEEP	50
	CLR	A0
	MOVE	A0,@ARWOFF		;STUFF 1 INTO FLAG TO KILL OFF
	MOVI	STUFFP,A0
	CALLA	GETFPAL
	MOVE	A0,A8
	SLL	5,A9
	ADDI	ARWLST,A9
;TURN ON PROPER ARROW
AON0	MOVI	01AH,A10
AON1	MOVE	A8,A1
	MOVE	*A9,A14,L
	MOVI	0600148H,A3
	MOVE	A13,-*SP,L
	CALLR	ABLOWP
	MOVE	*SP+,A13,L
	SLEEPK	1
	DSJS	A10,AON1
	SLEEPK	0FH
	MOVE	@ARWOFF,A0
	JRZ	AON0
	DEC	A0
	MOVE	A0,@ARWOFF
	CMPI	1,A0
	JRNZ	AON0
	DIE

