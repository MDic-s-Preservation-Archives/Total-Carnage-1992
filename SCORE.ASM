STUFF_LVADDR
;STUFF ADDR OF LIVES LEFT IMAGE TO BE BLOWN OUT
;A11=P1/P2DATA
	MOVE	*A11(PLIVES),A4
	CMPI	10,A4			;NEVER 10 TO BE DISPLAYED!
	JRGE	EX1
